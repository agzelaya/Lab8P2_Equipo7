    public void refreshComboBoxUniverso(){
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) jc_universos.getModel();
        modelo.removeAllElements();
        for (Universo u : universos) {
            modelo.addElement(u);
        }
        jc_universos.setModel(modelo);
    }

